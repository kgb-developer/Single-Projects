    buildMap: function() {
        var rowItem, row, cell, curCell;

        function getMapActive() {
            var isActive = document.getElementsByClassName('hpc__map__cell active'),
                detailId;

            hpc.el.mapDetailContainer.className = 'activate';

            if(isActive.length > 0) {
                isActive[0].classList.remove('active');
            }

            this.classList.add('active');
            detailId = isActive[0].textContent;
            hpc.el.mapDetailContainer.innerHTML = hpc.getMapDetail(detailId);
        }

        for(var i = 0; i < hpc.data.map.length; i++) {
            rowItem = document.createElement('div');
            rowItem.setAttribute('class', 'hpc__map__row');
            hpc.el.mapContainer.appendChild(rowItem);

            for(var j = 0; j < hpc.data.map[i].row.length; j++) {
                curCell = hpc.data.map[i].row[j];

                cell            = document.createElement('div');
                hpc.el.isEmpty  = curCell.isEmpty;
                hpc.el.abbr     = curCell.abbr;
                type            = curCell.type;

                if(!hpc.el.isEmpty) {
                    cell.setAttribute('class', 'hpc__map__cell hpc__map__cell--' + type);
                    cell.innerHTML = '<span>'+ hpc.el.abbr +'</span>';
                    cell.addEventListener('click', getMapActive);
                } else {
                    cell.setAttribute('class', 'hpc__map__cell hpc__map__cell--blank');
                    hpc.el.abbr = '';
                }

                rowItem.appendChild(cell);
            }
        }
    },

