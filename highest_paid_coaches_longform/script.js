            mapDropdownListItem.setAttribute('id', setAbbr);
            mapDropdownListItem.innerHTML = stateKey;
            mapDropdownList.appendChild(mapDropdownListItem);
            mapDropdownListItem.addEventListener('click', hpc.updateMapMenu);
