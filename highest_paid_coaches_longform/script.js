    updateMapMenu: function() {
        var getListItemId       = this.getAttribute('id'),
            getListItemText     = this.innerText,
            curCell             = document.getElementsByClassName('hpc__map__cell'),
            curActive           = document.getElementsByClassName('hpc__map__cell active'),
            searchVal           = getListItemId,
            test                = document.querySelector('.hpc__map-dropdown__list'),
            curCellActive;

        hpc.el.mapDropdownSelected.innerHTML = getListItemText;
        hpc.el.mapDetailContainer.innerHTML = hpc.getMapDetail(getListItemId);
        hpc.el.mapDropdown.classList.remove('hover');

        for(var i = 0; i < curCell.length; i++) {
            if(curCell[i].innerHTML.indexOf(searchVal) > -1){
                if(curActive.length > 0) {
                    curActive[0].classList.remove('active');
                }

                curCellActive = curCell[i];
                curCellActive.className += ' active';
            }
        }
    },

    buildSort: function() {
        var expandBtn           = document.querySelector('.hpc__expand-content'),
            sortSalaryBtn       = document.getElementById('sortSalary'),
            sortFootballBtn     = document.getElementById('sortFootball'),
            sortBasketballBtn   = document.getElementById('sortBasketball'),
            isActive            = document.getElementsByClassName('hpc__sorting__btn active'),
            hook                = true;

        function sortBy(sortType) {
            var sortContainer   = document.getElementById('sort-me'),
                toSort          = document.getElementsByClassName('hpc__bar-chart__wrapper--sort'),
                dataAttr        = 'data-salary',
                sortArr         = [];

            for(var i = 0; i < toSort.length; i++) {
                sortArr.push(toSort[i]);
            }

            if(sortType == "ascending") {
                sortArr.sort(function(a, b) {
                    return +a.getAttribute(dataAttr) - +b.getAttribute(dataAttr);
                });
            } else if(sortType == "descending") {
                sortArr.sort(function(a, b) {
                    return +b.getAttribute(dataAttr) - +a.getAttribute(dataAttr);
                });
            } else {
                sortArr.sort(function(a, b) {
                    return +b.getAttribute(dataAttr) - +a.getAttribute(dataAttr);
                });
            }

            sortArr.forEach(function(el) {
                sortContainer.appendChild(el, toSort);
            });
        }

        function sortToggle(btn) {
            var desc        = 'descending',
                asc         = 'ascending',
                isToggle    = btn.classList.contains('active'),
                isAsc       = btn.classList.contains(asc);

            if(isActive.length > 0) {
                isActive[0].setAttribute('class', 'hpc__sorting__btn');
            }

            btn.classList.add('active');

            if (isToggle) {
                if (isAsc) {
                    sortBy(desc);
                    btn.classList.remove(asc);
                    btn.classList.add(desc);
                } else {
                    sortBy(asc);
                    btn.classList.add(asc);
                    btn.classList.remove(desc);
                }
            } else {
                sortBy(desc);
                btn.classList.add(desc);
            }
        }

        expandBtn.addEventListener('click', function() {
            hpc.el.sortList.classList.toggle('active');

            if(hook) {
                this.innerHTML = "Collapse Full List";
                hpc.el.sortList.classList.add('active');
                hook = false;
            } else {
                this.innerHTML = "Expand Full List";
                hpc.el.sortList.classList.remove('active');
                hook = true;
            }
        });

        sortSalaryBtn.addEventListener('click', function() {
            hpc.el.sortListContainer.innerHTML = '';

            for(stateKey in hpc.data.states) {
                hpc.el.sortListContainer.innerHTML += hpc.getSortDetail(hpc.el.isFootball) + hpc.getSortDetail(hpc.el.isBasketball);
            }

            sortToggle(this);
        });

        sortFootballBtn.addEventListener('click', function() {
            hpc.el.sortListContainer.innerHTML = '';

            for(stateKey in hpc.data.states) {
                hpc.el.sortListContainer.innerHTML += hpc.getSortDetail(hpc.el.isFootball);
            }

            sortToggle(this);
        });

        sortBasketballBtn.addEventListener('click', function() {
            hpc.el.sortListContainer.innerHTML = '';

            for(stateKey in hpc.data.states) {
                hpc.el.sortListContainer.innerHTML += hpc.getSortDetail(hpc.el.isBasketball);
            }

            sortToggle(this);
        });
    },

    getSortDetail: function(type) {
        var key = hpc.data.states[stateKey],
            isSalary, isDivisor;

        if(key.hasOwnProperty(type)) {
            if(type == hpc.el.isFootball) {
                type = key.football;
            } else if(type == hpc.el.isBasketball) {
                type = key.basketball;
            }

            isSalary = type.salary.split('M').join('');
            isDivisor = isSalary.split(',').join('');

            if(isDivisor > 9.9) {
                isDivisor = isDivisor / 1000000;
            }

            return (
                '<div class="hpc__bar-chart__wrapper hpc__bar-chart__wrapper--sort" data-salary="'+ isDivisor +'">' +
                    '<div class="hpc__bar-chart__headshot"><img src="'+ type.imageURL +'" alt="" width="32"></div>' +
                    '<div class="hpc__bar-chart__content">' +
                        '<div class="hpc__bar-chart__detail">' +
                            '<div class="hpc__bar-chart__name">'+ type.name +' <span>'+ type.title +'</span></div>' +
                            '<div class="hpc__bar-chart__salary">$'+ type.salary +'</div>' +
                        '</div>' +
                        '<div class="hpc__bar-chart__bar">' +
                            '<div class="hpc__bar-chart__animate" style="background:'+ type.keyColor +'; width: '+ type.average +'%;"></div>' +
                        '</div>' +
                    '</div>' +
                '</div>'
            )
        } else {
            return ''
        }
    },

    tabs: function(e, sport) {
        var tabWrap     = document.getElementsByClassName('hpc__tabs__wrapper'),
            tabBtn      = document.getElementsByClassName('hpc__tabs__nav__btn');

        e = e || window.event;

        var o = e.srcElement || e.target;

        for(i = 0; i < tabWrap.length; i++) {
            tabWrap[i].style.display = "none";
        }

        for(i = 0; i < tabBtn.length; i++) {
            tabBtn[i].className = tabBtn[i].className.replace(" active", "");
        }

        document.getElementById(sport).style.display = "block";
    },

